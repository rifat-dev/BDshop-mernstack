const { model, Schema } = require("mongoose");

const CategoriesSchema = new Schema(
  {
    name: {
      type: String,
      required: true,
      trim: true,
    },
    description: {
      type: String,
      trim: true,
    },
    showHome: {
      type: Boolean,
      default: false,
    },
    isActive: {
      type: Boolean,
      default: true,
    },
  },
  { timestamps: true }
);

const Categories = model("Categories", CategoriesSchema);
module.exports = Categories;
